class Ex11{ 
	public static void main(String[] args){
	//관계연산자
		int su = 5;

		System.out.println(su > 3);
		System.out.println(su < 3);
		System.out.println(su >= 3);
		System.out.println(su <= 3);
		System.out.println(su ==3 );
		// boolean d = su ==2;
		System.out.println(su != 3);
		System.out.println("-------------------------------------------");
		//논리연산자(&&,||)
		System.out.println(true && true);
		System.out.println(true && false);
		System.out.println(false && true);
		System.out.println(false && false);
		System.out.println(true || true);
		System.out.println(true || false);
		System.out.println(false || true);
		System.out.println(false || false);
	}
}