package src.com.bit.day11;

public class Ex05 {
    public static void main(String[] args) {
        char ch1 = ' ';
        Character ch2 = new Character(ch1);//캐릭터에는 문자열은 들어갈수없고 캐릭터만 들어와야합니다.
        System.out.println(Character.BYTES);
        System.out.println(Character.SIZE);
        System.out.println(Character.MIN_VALUE);
        System.out.println(Character.MAX_VALUE);

        //유니코드 문자인지
        System.out.println("유니코드인지:"+Character.isDefined(ch1));
        System.out.println("숫자인지:"+Character.isDigit(ch1));
        // if '0' <= ch1 <= '9'
        System.out.println("문자 인지:"+Character.isAlphabetic(ch1));//알파뱃인지 검사하는것이 아니라 문자열을 검사하여줍니다.
        System.out.println("문자 인지:"+Character.isLetter(ch1));
        //대소구분
        System.out.println("대문자인가 인지:"+Character.isUpperCase(ch1));
        System.out.println("소문자인가 인지:"+Character.isLowerCase(ch1));

        //띄어쓰기
        System.out.println("띄어쓰기:"+Character.isSpace(ch1));
        System.out.println("띄어쓰기:"+Character.isWhitespace(ch1));
    }
}
